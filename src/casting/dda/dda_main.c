void	reset_img(t_program *data)
{
	if (data->img)
		mlx_delete_image(data->mlx, data->img);
	data->img = mlx_new_image(data->mlx, SCR_WIDTH, SCR_HEIGHT);
	if (!data->img)
	{
		print_error("Error\nmlx_new_image() failure\n");
		cleanup(data, EXIT_FAILURE);
	}
	if (mlx_image_to_window(data->mlx, data->img, 0, 0) == -1)
	{
		print_error("Error\nmmlx_image_to_window failure\n");
		cleanup(data, EXIT_FAILURE);
	}
}

static void	pixels_to_image(t_program *data)
{
	int	y;
	int	x;

	y = 0;
	while (y < SCR_HEIGHT)
	{
		x = 0;
		while (x < SCR_WIDTH)
		{
			mlx_put_pixel(data->img, x, y, data->img_buffer[y][x]);
			x++;
		}
		y++;
	}
}

void	dda_loop(void *param)
{
	t_program	*data;
	int			x;

	data = (t_program *)param;
	reset_img(data);
	x = 0;
	while (x < SCR_WIDTH)
	{
		dda_per_x(data->dda, x);
		draw_info_calc(data->dda);
		fill_buffer(data->input, data->img_buffer, data->dda, x);
		x++;
	}
	pixels_to_image(data);
	// keyInput(data);
}
